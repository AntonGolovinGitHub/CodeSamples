package tech.getrealgood.util;

import java.util.Collection;

/**
 * Set of generic methods for Object types.
 */
public final class Objects {

    /**
     *
     * @param o
     * @return
     */
    public static boolean isNull(Object o) {
        return o == null;
    }

    /**
     *
     * @param o
     * @return
     */
    public static boolean isNotNull(Object o) {
        return o != null;
    }

}